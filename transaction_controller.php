        if(session()->has('id'))
        {
            if(\session()->get('level')==0)
            {
                $data=transaction::orderByRaw('date desc,time desc')->paginate(20);
                return view('admin.transactions',compact('data'));
            }
            elseif(\session()->get('level')==1)
            {
                return  redirect('/teacher');
            }
            elseif(\session()->get('level')==2)
            {
                $account=session()->get('id');
                $data=transaction::where('account_id',$account)->orderByRaw('date desc,time desc')->paginate(20);
                return view('student.transactions',compact('data'));
            }
        }
        else
        {
            return  redirect('/');
        }


    }

